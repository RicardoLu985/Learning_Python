# 文件
"""
    文件是以计算机硬盘为载体在计算机上的信息集合，文件可以是文本文档、图片、程序等等。
    计算机文件基本上分为两种：二进制文件（没有统一的字符编码）和纯文本文件（有统一的编码，可以看做存储在磁盘上的长字符串）。
    纯文本文件编码格式常见的有：ASCII、ISO-8859-1、GB2312、GBK、UTF-8、UTF-16等。
    二进制文件与文本文件的一个最主要的区别在于是否有统一的字符编码格式，二进制文件顾名思义是直接由0和1组成，无统一的字符编码。
    如图片文件（jpg、png）、视频文件（avi）等。
"""

